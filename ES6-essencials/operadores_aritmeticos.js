// Módulo (%)
// Operador binário. Retorna o inteiro restante da divisão dos dois operandos.

//operador aritmético - MOD

12 % 5 // retorna 2.

// Incremento (++) e Decremento (--)

++x
x++

const a = ++2; // 3
const b = 2++; // 2

--x
x--

//Negação (-) e Adição (+)
-3
+"3"      //  retorna  3 // força string para numérico
+true     //  retorna  1 // true tem valor de 1 
+false    //  retorna  0 // false tem valor de 0
-true     //  retorna -1 


// Operador de exponenciação (**)
2 ** 3    //  retorna 8
10 ** -1  //  retorna 0.1


// Operador de agrupamento ()
2 * (3 + 2)

